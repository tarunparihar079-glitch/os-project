#include "io.h"

#include "idt.h"

struct idt_entry_struct idt_entries[256];

void init_idt() {

    pic_remap();

}

void pic_remap() {

    outb(0x20, 0x11); outb(0xA0, 0x11);

    outb(0x21, 0x20); outb(0xA1, 0x28);

    outb(0x21, 0x04); outb(0xA1, 0x02);

    outb(0x21, 0x01); outb(0xA1, 0x01);

    outb(0x21, 0x0);  outb(0xA1, 0x0);

}
